create extension if not exists pgcrypto;

do $$
begin
  if not exists (select 1 from pg_type where typname = 'app_role' and typnamespace = 'public'::regnamespace) then
    create type public.app_role as enum ('user', 'admin');
  end if;
  if not exists (select 1 from pg_type where typname = 'prediction_status' and typnamespace = 'public'::regnamespace) then
    create type public.prediction_status as enum ('BORRADOR', 'CONFIRMADO', 'BLOQUEADO_POR_FECHA');
  end if;
  if not exists (select 1 from pg_type where typname = 'match_status' and typnamespace = 'public'::regnamespace) then
    create type public.match_status as enum ('scheduled', 'played');
  end if;
end $$;

create table if not exists public.users (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text not null,
  email text not null unique,
  role public.app_role not null default 'user',
  created_at timestamptz not null default now()
);

create table if not exists public.matches (
  id bigint generated by default as identity primary key,
  match_number int not null unique,
  phase text not null,
  group_name text,
  team_a text not null,
  team_b text not null,
  match_date timestamptz,
  venue text,
  status public.match_status not null default 'scheduled',
  sort_order int not null
);

create table if not exists public.predictions (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.users(id) on delete cascade,
  status public.prediction_status not null default 'BORRADOR',
  confirmed_at timestamptz,
  confirmation_code text unique,
  ip_address inet,
  user_agent text,
  validation_hash text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (user_id)
);

create table if not exists public.prediction_details (
  id uuid primary key default gen_random_uuid(),
  prediction_id uuid not null references public.predictions(id) on delete cascade,
  match_id bigint not null references public.matches(id) on delete cascade,
  predicted_goals_a int check (predicted_goals_a between 0 and 99),
  predicted_goals_b int check (predicted_goals_b between 0 and 99),
  points int not null default 0,
  exact_score boolean not null default false,
  winner_hit boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (prediction_id, match_id)
);

create table if not exists public.results (
  id uuid primary key default gen_random_uuid(),
  match_id bigint not null unique references public.matches(id) on delete cascade,
  goals_a int not null check (goals_a between 0 and 99),
  goals_b int not null check (goals_b between 0 and 99),
  registered_by uuid references public.users(id),
  registered_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.rankings (
  user_id uuid primary key references public.users(id) on delete cascade,
  full_name text not null,
  total_points int not null default 0,
  exact_scores int not null default 0,
  winner_hits int not null default 0,
  matches_hit int not null default 0,
  position int not null default 0,
  updated_at timestamptz not null default now()
);

create table if not exists public.settings (
  key text primary key,
  value text not null,
  updated_at timestamptz not null default now()
);

create table if not exists public.audit_logs (
  id bigint generated always as identity primary key,
  user_id uuid references public.users(id),
  action text not null,
  entity text not null,
  entity_id text,
  metadata jsonb not null default '{}'::jsonb,
  ip_address inet,
  user_agent text,
  created_at timestamptz not null default now()
);

create or replace view public.confirmed_predictions as
select u.full_name, p.confirmed_at, p.status::text as status
from public.predictions p
join public.users u on u.id = p.user_id
where p.status = 'CONFIRMADO'
order by p.confirmed_at desc;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (select 1 from public.users where id = auth.uid() and role = 'admin');
$$;

create or replace function public.log_audit(p_action text, p_entity text, p_entity_id text default null, p_metadata jsonb default '{}'::jsonb)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  headers jsonb := coalesce(nullif(current_setting('request.headers', true), '')::jsonb, '{}'::jsonb);
begin
  insert into public.audit_logs(user_id, action, entity, entity_id, metadata, ip_address, user_agent)
  values (
    auth.uid(),
    p_action,
    p_entity,
    p_entity_id,
    p_metadata,
    nullif(split_part(coalesce(headers->>'x-forwarded-for', ''), ',', 1), '')::inet,
    headers->>'user-agent'
  );
end;
$$;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.users(id, full_name, email)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'full_name', split_part(new.email, '@', 1)),
    new.email
  )
  on conflict (id) do update set email = excluded.email;
  return new;
end;
$$;

