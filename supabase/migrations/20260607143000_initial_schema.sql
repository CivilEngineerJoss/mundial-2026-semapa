create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

create or replace function public.score_prediction(p_pred_a int, p_pred_b int, p_real_a int, p_real_b int)
returns table(points int, exact_score boolean, winner_hit boolean)
language sql
immutable
as $$
  select
    case
      when p_pred_a is null or p_pred_b is null or p_real_a is null or p_real_b is null then 0
      when p_pred_a = p_real_a and p_pred_b = p_real_b then 3
      when sign(p_pred_a - p_pred_b) = sign(p_real_a - p_real_b) then 1
      else 0
    end,
    p_pred_a = p_real_a and p_pred_b = p_real_b,
    not (p_pred_a = p_real_a and p_pred_b = p_real_b) and sign(p_pred_a - p_pred_b) = sign(p_real_a - p_real_b);
$$;

create or replace function public.recalculate_rankings()
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  update public.prediction_details pd
  set
    points = s.points,
    exact_score = s.exact_score,
    winner_hit = s.winner_hit,
    updated_at = now()
  from (
    select
      pd2.id,
      scored.points,
      scored.exact_score,
      scored.winner_hit
    from public.prediction_details pd2
    join public.results r on r.match_id = pd2.match_id
    cross join lateral public.score_prediction(pd2.predicted_goals_a, pd2.predicted_goals_b, r.goals_a, r.goals_b) scored
  ) s
  where s.id = pd.id;

  delete from public.rankings where true;

  insert into public.rankings(user_id, full_name, total_points, exact_scores, winner_hits, matches_hit, position, updated_at)
  select
    ranked.user_id,
    ranked.full_name,
    ranked.total_points,
    ranked.exact_scores,
    ranked.winner_hits,
    ranked.matches_hit,
    ranked.position,
    now()
  from (
    select
      u.id as user_id,
      u.full_name,
      coalesce(sum(pd.points), 0)::int as total_points,
      count(*) filter (where pd.exact_score)::int as exact_scores,
      count(*) filter (where pd.winner_hit)::int as winner_hits,
      count(*) filter (where pd.exact_score or pd.winner_hit)::int as matches_hit,
      row_number() over (
        order by coalesce(sum(pd.points), 0) desc,
        count(*) filter (where pd.exact_score) desc,
        count(*) filter (where pd.exact_score or pd.winner_hit) desc,
        u.full_name asc
      )::int as position
    from public.users u
    join public.predictions p on p.user_id = u.id and p.status = 'CONFIRMADO'
    left join public.prediction_details pd on pd.prediction_id = p.id
    group by u.id, u.full_name
  ) ranked;
end;
$$;

create sequence if not exists public.confirmation_code_seq start with 1;

create or replace function public.confirm_prediction(p_prediction_id uuid, p_user_agent text, p_validation_hash text)
returns text
language plpgsql
security definer
set search_path = public
as $$
declare
  owner_id uuid;
  total_matches int;
  total_details int;
  next_code text;
  headers jsonb := coalesce(nullif(current_setting('request.headers', true), '')::jsonb, '{}'::jsonb);
begin
  select user_id into owner_id from public.predictions where id = p_prediction_id;
  if owner_id is null or owner_id <> auth.uid() then
    raise exception 'Pronostico no autorizado';
  end if;
  if now() >= (select value::timestamptz from public.settings where key = 'deadline_iso') then
    raise exception 'La fecha limite ya vencio';
  end if;
  select count(*) into total_matches from public.matches;
  select count(*) into total_details
  from public.prediction_details
  where prediction_id = p_prediction_id
    and predicted_goals_a is not null
    and predicted_goals_b is not null;
  if total_details <> total_matches then
    raise exception 'Debe completar todos los partidos antes de confirmar';
  end if;

  next_code := 'SEMAPA-2026-' || lpad(nextval('public.confirmation_code_seq')::text, 6, '0');

  update public.predictions
  set status = 'CONFIRMADO',
      confirmed_at = now(),
      confirmation_code = next_code,
      ip_address = nullif(split_part(coalesce(headers->>'x-forwarded-for', ''), ',', 1), '')::inet,
      user_agent = p_user_agent,
      validation_hash = p_validation_hash,
      updated_at = now()
  where id = p_prediction_id and status = 'BORRADOR';

  perform public.log_audit('CONFIRMACION_DEFINITIVA', 'predictions', p_prediction_id::text, jsonb_build_object('code', next_code));
  perform public.recalculate_rankings();
  return next_code;
end;
$$;

create or replace function public.register_result(p_match_id bigint, p_goals_a int, p_goals_b int)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_admin() then
    raise exception 'Solo administradores';
  end if;
  insert into public.results(match_id, goals_a, goals_b, registered_by)
  values (p_match_id, p_goals_a, p_goals_b, auth.uid())
  on conflict (match_id) do update
  set goals_a = excluded.goals_a,
      goals_b = excluded.goals_b,
      registered_by = auth.uid(),
      updated_at = now();

  update public.matches set status = 'played' where id = p_match_id;
  perform public.log_audit('REGISTRO_RESULTADO', 'results', p_match_id::text, jsonb_build_object('goals_a', p_goals_a, 'goals_b', p_goals_b));
  perform public.recalculate_rankings();
end;
$$;

create or replace function public.admin_dashboard()
returns jsonb
language sql
security definer
set search_path = public
as $$
  select jsonb_build_object(
    'registered', (select count(*) from public.users),
    'confirmed', (select count(*) from public.predictions where status = 'CONFIRMADO'),
    'pending', greatest((select count(*) from public.users) - (select count(*) from public.predictions where status = 'CONFIRMADO'), 0),
    'played', (select count(*) from public.matches where status = 'played'),
    'pending_matches', (select count(*) from public.matches where status <> 'played')
  )
  where public.is_admin();
$$;

